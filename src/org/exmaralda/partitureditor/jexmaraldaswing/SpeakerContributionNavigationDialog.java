    private void gotoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gotoButtonActionPerformed
        // Add your handling code here:
        int index = scList.getSelectedIndex();
        SpeakerContribution sc = (SpeakerContribution)(speakerContributions.elementAt(index));
        String start = sc.getMain().getStart();
        String correspondingStart = (String)(tliMappings.get(start));
        System.out.println(correspondingStart);
        
        // make a dummy search result and send it to the table
        // so that it scrolls to the offending positiion
        org.exmaralda.partitureditor.search.EventSearchResult esr = new org.exmaralda.partitureditor.search.EventSearchResult();
        Event ev = new Event();
        ev.setStart(correspondingStart);
        esr.event = ev;
        esr.tierID = "";
        table.processSearchResult(esr);        
    }//GEN-LAST:event_gotoButtonActionPerformed
    
