    public SpeakerContributionNavigationDialog( java.awt.Frame parent, boolean modal, 
                                                Vector scs, Hashtable tlim, PartitureTableWithActions t) {
        super(parent, modal);
        tliMappings = tlim;
        speakerContributions = scs;
        table = t; 
        descriptions = new String[scs.size()];
        for (int pos=0; pos<speakerContributions.size(); pos++){
            SpeakerContribution sc = (SpeakerContribution)(speakerContributions.elementAt(pos));
            String desc = sc.getMain().getDescription();
            descriptions[pos] = desc;            
        }        
        initComponents();
        scList.setSelectedIndex(0);
        Internationalizer.internationalizeDialogToolTips(this);        
    }
    
