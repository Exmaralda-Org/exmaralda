    public EditScaleConstantDialog(java.awt.Frame parent, boolean modal, int sc) {
        super(parent, modal);
        initComponents();
        this.getRootPane().setDefaultButton(okButton);
        labelTable = new Hashtable();
        for (int pos=0; pos<=20; pos+=5){
            javax.swing.JLabel label = new javax.swing.JLabel(Integer.toString(pos-10));
            labelTable.put(pos, label);
        }
        valueSlider.setLabelTable(labelTable);
        oldConstant=sc;
        valueSlider.setValue(sc+10);
        Internationalizer.internationalizeDialogToolTips(this);        
    }
    
    public int getScaleConstant(){
        return valueSlider.getValue()-10;
    }

