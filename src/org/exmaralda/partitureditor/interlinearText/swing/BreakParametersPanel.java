    private void updateWordBoundarieLabel() {
        String wec = new String();
        for (int pos=0; pos<wordEndChars.length; pos++){
            wec+="[" + wordEndChars[pos] + "] ";
        }
        wordBoundariesLabel.setText(wec);
    }
