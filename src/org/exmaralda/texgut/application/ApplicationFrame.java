    private void transcriptFolderBrowseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transcriptFolderBrowseButtonActionPerformed
        JFileChooser jfc = new JFileChooser();
        jfc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        jfc.setDialogTitle("Choose top level folder for transcript files");
        int r = jfc.showOpenDialog(this.mainPanel);
        if (r==JFileChooser.APPROVE_OPTION){
            transcriptFolderTextField.setText(jfc.getSelectedFile().getAbsolutePath());
            java.util.prefs.Preferences.userRoot().node(getPreferencesNode()).put("transcript-folder", jfc.getSelectedFile().getAbsolutePath());            
        }

    }//GEN-LAST:event_transcriptFolderBrowseButtonActionPerformed

