    private void browseComaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseComaButtonActionPerformed
           JFileChooser fileChooser = new JFileChooser();
           fileChooser.setDialogTitle("Specify Coma file to create");
           fileChooser.setFileFilter(new ParameterFileFilter("coma", "Corpus Manager File (*.coma)"));
           fileChooser.setCurrentDirectory(lastDirectory);
           if (fileChooser.showSaveDialog(this)==JFileChooser.APPROVE_OPTION){
               lastDirectory = fileChooser.getSelectedFile().getParentFile();
               String text = fileChooser.getSelectedFile().getAbsolutePath();
               if (!text.endsWith(".coma")){
                   text+=".coma";
               }
               comaTextField.setText(text);
           }
}//GEN-LAST:event_browseComaButtonActionPerformed


