    public List<AdvancedSortDirective> getSortDirectives(){
        List<AdvancedSortDirective> result = new ArrayList<>();
        for (AdvancedSortPanel sortPanel : sortPanels){
            if (sortPanel.isActive()){
                result.add(sortPanel.getSortDirective());
            }
        }        
        return result;
    }

