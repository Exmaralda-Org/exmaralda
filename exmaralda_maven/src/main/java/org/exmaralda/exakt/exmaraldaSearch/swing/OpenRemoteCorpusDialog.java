    private void openCatalogButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openCatalogButtonActionPerformed
        CatalogLookupDialog cld = new CatalogLookupDialog(parentFrame,true);
        cld.setLocationRelativeTo(this);
        cld.setVisible(true);
        if (cld.approved){
            urlTextField.setText(cld.getCOMAURL());
            boolean anonymous = "anonymous".equals(cld.getLoginType());
            anonymousCheckBox.setSelected(anonymous);
            anonymousCheckBoxActionPerformed(null);
        }
    }//GEN-LAST:event_openCatalogButtonActionPerformed

