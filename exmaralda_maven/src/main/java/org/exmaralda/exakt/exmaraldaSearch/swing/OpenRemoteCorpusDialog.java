    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if (this.getParent() instanceof ExmaraldaApplication){
            ExmaraldaApplication ea = (ExmaraldaApplication)getParent();
            Preferences prefs = java.util.prefs.Preferences.userRoot().node(ea.getPreferencesNode());
            prefs.put("Remote-Corpus", urlTextField.getText());
            prefs.put("Remote-Corpus-Username", usernameTextField.getText());
        }
        approved = true;
        dispose();
    }//GEN-LAST:event_okButtonActionPerformed

