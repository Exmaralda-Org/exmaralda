    public void setFiles(Vector<File> files){
        model = new FileSelectionTableModel(files);
        filesTable.setModel(model);
        filesTable.getColumnModel().getColumn(0).setPreferredWidth(150);
        filesTable.getColumnModel().getColumn(1).setPreferredWidth(2000);
        filesTable.getColumnModel().getColumn(0).setHeaderValue("Include");
        filesTable.getColumnModel().getColumn(1).setHeaderValue("Transcription");
        FileSelectionTableCellRenderer renderer = new FileSelectionTableCellRenderer();
        filesTable.getColumnModel().getColumn(0).setCellRenderer(renderer);
        filesTable.getColumnModel().getColumn(1).setCellRenderer(renderer);
        filesTable.setRowHeight(22);
        filesTable.setSelectionMode(filesTable.getSelectionModel().SINGLE_INTERVAL_SELECTION);

        countLabel.setText(Integer.toString(files.size()) + " of " + Integer.toString(files.size()) + " selected.");

    }

    Vector<File> getFiles() {
        return model.getSelected();
    }

