    public abstract int getNumberOfSteps();
    
    public abstract String getStep(int n);
        
    public void initialise(){
        initComponents();
        for (int pos=0; pos<getNumberOfSteps(); pos++){
            String desc = getStep(pos);
            JLabel label = new JLabel(desc);
            label.setForeground(java.awt.Color.BLUE);
            label.setBorder(new javax.swing.border.EmptyBorder(5,5,5,5));
            stepsPanel.add(label);
            mainPanel.add(stepPanels[pos]);
            stepPanels[pos].setVisible(false);
        }
        
        gotoStep(0);
    }

    public abstract void loadSettings();

    public abstract void storeSettings();

    public boolean isComplete(){
        return complete;
    }
      
    public void gotoStep(int n){
        for (int pos=0; pos<getNumberOfSteps(); pos++){
            JLabel label = (JLabel)(stepsPanel.getComponent(pos));
            if (pos==n){
                label.setFont(label.getFont().deriveFont(java.awt.Font.BOLD));
            } else {
                label.setFont(label.getFont().deriveFont(java.awt.Font.PLAIN));                
            }
            stepPanels[pos].setVisible(pos==n);
        }
                
        mainPanel.validate();
        
        currentStep = n;
        backButton.setEnabled(n>0);
        //nextButton.setEnabled(n<getNumberOfSteps()-1);
        if (n<getNumberOfSteps()-1){
            nextButton.setText("Next >");
        } else {
            nextButton.setText("Finish");
        }

        pack();
    }

