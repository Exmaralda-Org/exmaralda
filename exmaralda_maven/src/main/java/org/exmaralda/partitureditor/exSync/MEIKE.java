    void copyMeta(BasicTranscription source, BasicTranscription target, String name) throws Exception{
        target.getHead().setMetaInformation(source.getHead().getMetaInformation());
        Speakertable st1 = source.getHead().getSpeakertable();
        Speakertable st2 = target.getHead().getSpeakertable();
        for (int pos=0; pos<st2.getNumberOfSpeakers(); pos++){
            Speaker sp = st2.getSpeakerAt(pos);
            for (int i=0; i<st1.getNumberOfSpeakers(); i++){
                Speaker sp2 = st1.getSpeakerAt(i);
                if (sp2.getAbbreviation().equals(sp.getAbbreviation())){
                    sp.setComment(sp2.getComment());
                    sp.setL1(sp2.getL1());
                    sp.setL2(sp2.getL2());
                    sp.setLanguagesUsed(sp2.getLanguagesUsed());
                    sp.setSex(sp2.getSex());
                    sp.setUDSpeakerInformation(sp2.getUDSpeakerInformation());
                    break;
                }
            }
        }
        target.writeXMLToFile(name, "none");
    }
