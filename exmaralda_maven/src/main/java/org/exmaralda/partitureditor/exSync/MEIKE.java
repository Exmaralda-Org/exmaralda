    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        BasicTranscription vorlage = null;
        try {
            vorlage = new BasicTranscription(jTextField1.getText());
        } catch (Exception e){
            e.printStackTrace();
            String message = "Vorlage konnte nicht gelesen werden.";
            message+=System.getProperty("line.separator") + e.getLocalizedMessage();
            javax.swing.JOptionPane.showMessageDialog(this, message);
            return;
        }
        String name = "";
        try{
            for (int pos=0; pos<model.size(); pos++){
                name = ((java.io.File)(model.getElementAt(pos))).getAbsolutePath();
                BasicTranscription bt = new BasicTranscription(name);
                copyMeta(vorlage,bt,name);
            }
        } catch (Exception e){
            e.printStackTrace();
            String message2 = name + " konnte nicht bearbeitet werden.";
            message2+=System.getProperty("line.separator") + e.getLocalizedMessage();
            javax.swing.JOptionPane.showMessageDialog(this, message2);
            return;            
        }
        String message3 = "Fertig!";
        javax.swing.JOptionPane.showMessageDialog(this, message3);
        
    }//GEN-LAST:event_jButton3ActionPerformed

