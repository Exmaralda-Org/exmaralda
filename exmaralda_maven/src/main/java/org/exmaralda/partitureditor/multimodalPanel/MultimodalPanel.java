    protected void fireUnicodeKeyPressed(String t) {
        UnicodeKeyboardEvent event = new UnicodeKeyboardEvent(t);
        for (UnicodeKeyboardListener listener : listeners) {
            listener.performUnicodeKeyboardAction(event);             
         }
    }
    
    public void addUnicodeKeyboardListener(UnicodeKeyboardListener listener){
        listeners.add(listener);
    }
    
    public void removeAllListeners() {
        listeners.removeAll(listeners);
    }
    

    private void updatePeriodButton(){
        int length = ((Integer) milisecondsSpinner.getValue()).intValue();
        String type = (String) typeComboBox.getSelectedItem();
        String period = "";
        for (int i=0; i<length; i++){
            if ("Preparation".equals(type)) period+=".";
            if ("Holding".equals(type)) period+="-";
            if ("Retraction".equals(type)) period+=",";
        }
        period+=">";
        periodButton.setText(period);        
    }
