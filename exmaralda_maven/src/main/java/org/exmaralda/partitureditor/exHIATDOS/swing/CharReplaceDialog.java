    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // Add your handling code here:
        try {
            int i1 = Integer.parseInt(jTextField1.getText());
            int i2 = Integer.parseInt(jTextField2.getText());
            if (i1<0 || i1 > 65535 || i2<0 || i2 > 65536) {
                throw new NumberFormatException();
            }
            char c1 = (char)i1;
            char c2 = (char)i2;
            char[] newc = {c1, c2};
            charReplace.addElement(newc);
            this.setListContent();
        } catch (NumberFormatException nfe){
                javax.swing.JOptionPane errorDialog = new javax.swing.JOptionPane();
                errorDialog.showMessageDialog(  this.getParent(),
                                        "Error in unicode numbers!",
                                        "Error",
                                        javax.swing.JOptionPane.ERROR_MESSAGE);              
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

