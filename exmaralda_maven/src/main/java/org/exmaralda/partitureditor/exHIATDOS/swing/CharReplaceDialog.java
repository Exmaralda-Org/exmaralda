    private void setListContent(){
        Vector listData = new Vector();
        for (int pos=0; pos<charReplace.size(); pos++){
            char[] c = (char[])(charReplace.elementAt(pos));
            int i1 = c[0];
            int i2 = c[1];
            String text = c[0] + " (" + i1 + ") " + "\u21A6" + " " + c[1] + " (" + i2 + ") ";
            listData.addElement(text);
        }
        jList1.setListData(listData);
        if (charReplace.size()>0){
            jList1.setSelectedIndex(0);
        }
    }
    
