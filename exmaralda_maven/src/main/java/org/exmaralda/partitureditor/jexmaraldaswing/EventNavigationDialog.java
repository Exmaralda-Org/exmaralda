    private void gotoButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Add your handling code here:
        int index = scList.getSelectedIndex();
        Object[] o = (Object[])(events.elementAt(index));
        Event e = (Event)(o[0]);
        String tierID = (String)(o[1]);
        String start = e.getStart();
        
        // make a dummy search result and send it to the table
        // so that it scrolls to the offending positiion
        org.exmaralda.partitureditor.search.EventSearchResult esr = new org.exmaralda.partitureditor.search.EventSearchResult();
        Event ev = new Event();
        ev.setStart(start);
        esr.event = e;
        esr.tierID = tierID;
        table.processSearchResult(esr);        
    }
    
